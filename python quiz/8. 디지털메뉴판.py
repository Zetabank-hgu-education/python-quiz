# 8. 디지털 메뉴판
# 여러분은 작은 레스토랑을 운영하고 있고, 디지털 메뉴판을 만들려고 합니다.
# 고객이 메뉴판에서 음식을 선택하면, 그 음식에 해당하는 이름이 화면에 출력되는 코드를 작성해 봅시다.

try:
    num = int(input("음식을 선택해주세요 (1: 피자, 2: 스파게티, 3: 햄버거): "))
    
    if num == 1:
        print("고객님이 선택한 음식은 '피자'입니다.")
    elif num == 2:
        print("고객님이 선택한 음식은 '스파게티'입니다.")
    elif num == 3:
        print("고객님이 선택한 음식은 '햄버거'입니다.")
    else:
        print("잘못된 선택입니다. 1, 2, 3 중에서 선택해주세요.")

except ValueError:
    print("유효한 숫자를 입력해주세요.")